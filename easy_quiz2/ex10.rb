# benefits of using Object Oriented Programming in Ruby
# 1. less repetition of code (DRY principle)
# 2. more manageable code, more maintaineable. we modify something in one place instead of all the repeated places
# 3. less border effects if we modify something
# 4. more logical because objects represent models of reality
# 5. better to work in teams (division of the work)
# 6. best reusability of code
# 7. encapsulation : the code is encapsulated so it permits to manage complexity easier.